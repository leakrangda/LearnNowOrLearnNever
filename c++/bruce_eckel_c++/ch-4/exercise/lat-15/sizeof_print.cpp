//sizeof_print.cpp
#include <iostream>
using namespace std;

int main(void)
{
	cout<<"char   :"<<sizeof(char)<<endl;
	cout<<"short  :"<<sizeof(short int)<<endl;
	cout<<"int    :"<<sizeof(int)<<endl;
	cout<<"long   :"<<sizeof(long int)<<endl;
	cout<<"float  :"<<sizeof(float)<<endl;
	cout<<"double :"<<sizeof(double)<<endl;
	return 0;
}
